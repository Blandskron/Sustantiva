# Creamos una lista inicial de números
numeros = [1, 2, 3, 4, 5]

# Mostramos la lista antes de insertar un nuevo elemento
print("Lista de números antes de insertar:", numeros)

# Insertamos el número 10 en la posición 2 (índice 1) de la lista
numeros.insert(1, 10)

# Mostramos la lista después de insertar el nuevo elemento
print("Lista de números después de insertar:", numeros)
