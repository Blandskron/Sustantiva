# Creamos una lista inicial
mi_lista = [1, 2, 3, 4, 5]

# Mostramos la lista antes de usar clear()
print("Lista antes de clear():", mi_lista)

# Utilizamos clear() para eliminar todos los elementos de la lista
mi_lista.clear()

# Mostramos la lista después de usar clear()
print("Lista después de clear():", mi_lista)
