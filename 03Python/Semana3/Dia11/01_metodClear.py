# Creamos una lista inicial con algunos elementos
frutas = ['manzana', 'banana', 'naranja', 'pera', 'uva']

# Mostramos la lista antes de limpiarla
print("Lista de frutas antes de limpiarla:", frutas)

# Utilizamos el método clear() para eliminar todos los elementos de la lista
frutas.clear()

# Mostramos la lista después de limpiarla
print("Lista de frutas después de limpiarla:", frutas)
