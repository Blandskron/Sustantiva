# Creamos un diccionario
mi_diccionario = {"nombre": "Juan", "edad": 30, "ciudad": "Madrid"}

# Eliminamos el elemento con la clave "edad" y almacenamos su valor
valor_eliminado = mi_diccionario.pop("edad")

# Imprimimos el valor eliminado y el diccionario actualizado
print("Valor eliminado:", valor_eliminado)
print("Diccionario actualizado:", mi_diccionario)
