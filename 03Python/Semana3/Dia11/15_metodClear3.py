# Creamos un diccionario
mi_diccionario = {"nombre": "Juan", "edad": 30, "ciudad": "Madrid"}

# Mostramos el diccionario antes de usar el método clear()
print("Diccionario antes de clear():", mi_diccionario)

# Aplicamos el método clear() para eliminar todos los elementos del diccionario
mi_diccionario.clear()

# Mostramos el diccionario después de usar el método clear()
print("Diccionario después de clear():", mi_diccionario)
