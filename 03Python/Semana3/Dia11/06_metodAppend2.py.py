# Creamos una lista inicial de frutas
frutas = ['manzana', 'banana', 'naranja']

# Mostramos la lista de frutas antes de agregar un nuevo elemento
print("Lista de frutas:", frutas)

# Agregamos una nueva fruta al final de la lista usando append()
frutas.append('uva')

# Mostramos la lista de frutas después de agregar la nueva fruta
print("Lista de frutas después de agregar 'uva':", frutas)
