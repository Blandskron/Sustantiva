# Creamos una lista inicial
lista_original = [1, 2, 3, 4, 5]

# Creamos una copia de la lista original usando el m√©todo copy()
copia_lista = lista_original.copy()

# Modificamos la copia de la lista agregando un elemento
copia_lista.append(6)

# Mostramos ambas listas para ver los cambios
print("Lista original:", lista_original)
print("Copia de la lista:", copia_lista)
