# Creamos un conjunto
mi_conjunto = {1, 2, 3, 4}

# Mostramos el conjunto antes de usar el método add()
print("Conjunto antes de add():", mi_conjunto)

# Agregamos un nuevo elemento al conjunto usando el método add()
mi_conjunto.add(5)

# Mostramos el conjunto después de usar el método add()
print("Conjunto después de add():", mi_conjunto)

# Intentamos agregar un elemento que ya existe en el conjunto
mi_conjunto.add(3)

# Mostramos el conjunto después de intentar agregar un elemento duplicado
print("Conjunto después de intentar agregar un elemento duplicado:", mi_conjunto)
