# Creamos una lista inicial de colores
colores = ['rojo', 'verde', 'azul', 'amarillo', 'rojo']

# Mostramos la lista antes de utilizar remove()
print("Lista de colores antes de utilizar remove():", colores)

# Utilizamos remove() para eliminar la primera ocurrencia del elemento 'rojo'
colores.remove('rojo')

# Mostramos la lista actualizada después de eliminar 'rojo'
print("Lista de colores después de utilizar remove():", colores)
