# Creamos una lista inicial de números
numeros = [1, 2, 3, 4, 5, 2, 3, 2, 1, 2]

# Mostramos la lista antes de utilizar count()
print("Lista de números:", numeros)

# Utilizamos count() para contar cuántas veces aparece el número 2 en la lista
cantidad_dos = numeros.count(2)

# Mostramos la cantidad de veces que aparece el número 2 en la lista
print("El número 2 aparece", cantidad_dos, "veces en la lista.")
