# Creamos una lista inicial de frutas
frutas = ['manzana', 'banana', 'naranja', 'pera', 'uva']

# Mostramos la lista antes de utilizar pop()
print("Lista de frutas antes de utilizar pop():", frutas)

# Utilizamos pop() para eliminar el último elemento de la lista y guardarlo en una variable
fruta_eliminada = frutas.pop()

# Mostramos la fruta eliminada y la lista actualizada
print("Fruta eliminada:", fruta_eliminada)
print("Lista de frutas después de utilizar pop():", frutas)
