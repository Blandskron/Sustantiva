# Definición de la función sumar()
def sumar(numero_1, numero_2):
    # Calcula la suma de los dos números recibidos como argumentos
    resultado_suma = numero_1 + numero_2
    # Retorna el resultado de la suma
    return resultado_suma

# Llama a la función sumar() con los argumentos 2 y 3, y asigna el resultado a la variable resultado
resultado = sumar(2, 3)

# Imprime el valor de la variable resultado
print(resultado)
