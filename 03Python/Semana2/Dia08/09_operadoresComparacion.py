'''
OPERADORES DE COMPARACIÓN
>
<
>= 
<= 
!=
'''

# Singo >
a = 4
b = 6
resultado = a > b
print(f"a > b {resultado}")

# Singo <
a = 4
b = 6
resultado = a < b
print(f"a < b {resultado}")

# Singo >=
a = 6
b = 6
resultado = a >= b
print(f"a >= b {resultado}")

# Singo <=
a = 5
b = 6
resultado = a <= b
print(f"a <= b {resultado}")

# Singo ==
a = 5
b = 6
resultado = a == b
print(f"a == b {resultado}")

# Singo !=
a = 5
b = 6
resultado = a != b
print(f"a != b {resultado}")

# IF
a = 50
if a > 0:
    print("A es un número positivo.")
