# Imprime un saludo simple
print("¡Hola mundo!")

# Define una variable 'edad' y la imprime
edad = 32
print(edad)

# Define variables 'nombre' y 'edad', e imprime un mensaje que las combina
nombre = "Ana"
print("Mi nombre es", nombre, "y tengo", edad, "años de edad.")

# Utiliza el método .format() para insertar valores en una cadena
print("Mi nombre es {} y tengo {} años de edad.".format(nombre, edad))

# Utiliza f-strings para insertar valores directamente en la cadena (Python 3.6+)
print(f"Mi nombre es {nombre} y tengo {edad} años de edad.")
