# Definición de una variable de tipo cadena utilizando comillas dobles
saludo = "¡Hola, mundo!"

# Definición de una variable de tipo caracter (string de longitud 1) utilizando comillas simples
letra = 'a'

# Imprime el valor de la variable saludo
print(saludo)

# Imprime el valor de la variable letra
print(letra)

# Definición de una variable de tipo cadena utilizando comillas simples y dobles dentro de la misma cadena
texto_comillas_simples = 'Esto puede ser un saludo:"¡Hola, mundo!"'
texto_comillas_dobles = "Esto puede ser un saludo:'¡Hola, mundo!'"

# Imprime el valor de la variable texto_comillas_simples
print(texto_comillas_simples)

# Imprime el valor de la variable texto_comillas_dobles
print(texto_comillas_dobles)
