# Definición de una cadena de caracteres
cadena = 'python'

# Iteración sobre cada letra en la cadena 'cadena'
for letra in cadena:
    # Verifica si la letra actual es 'p'
    if letra == 'p':
        # Si es 'p', pasa a la siguiente iteración del bucle
        continue
    # Si la letra no es 'p', imprime la letra
    print(letra)
