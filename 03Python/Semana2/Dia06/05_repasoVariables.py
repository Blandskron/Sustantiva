# Integer (entero): representa números enteros sin decimales
num_entero = 2

# Float (flotante): representa números con decimales
num_flotante = 2.0

# String (cadena de caracteres): representa texto
texto = "2"

# Boolean (booleano): representa valores de verdadero o falso
booleano = True

# List (lista): representa una colección ordenada y mutable de elementos
lista = [2, "dos", True]

# Tuple (tupla): representa una colección ordenada e inmutable de elementos
tupla = (2, "dos", True)

# Set (conjunto): representa una colección desordenada y sin elementos duplicados
conjunto = {2, "dos", True}

# Dictionary (diccionario): representa una colección de pares clave-valor
diccionario = {"clave": 2, "otra_clave": "dos", "ultima_clave": True}

