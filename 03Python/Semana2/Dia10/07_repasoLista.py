# Creamos una lista de frutas con algunos elementos repetidos
mis_frutas = ["manzana", "uva", "fresa", "manzana", "uva", "fresa"]
# Imprimimos la lista de frutas
print(mis_frutas)

# Creamos una lista de números
mis_numeros = [24, 38, 85, 19]
# Imprimimos la lista de números
print(mis_numeros)

# Creamos una lista que contiene diferentes tipos de datos: cadena, entero y booleano
mi_informacion = ["Lincy", 9, True]
# Imprimimos la lista que contiene información variada
print(mi_informacion)

# Imprimimos cada elemento de la lista de frutas utilizando su índice
# Los índices comienzan desde 0
print(mis_frutas[0])  # Imprime el primer elemento de la lista (manzana)
print(mis_frutas[1])  # Imprime el segundo elemento de la lista (uva)
print(mis_frutas[2])  # Imprime el tercer elemento de la lista (fresa)
print(mis_frutas[3])  # Imprime el cuarto elemento de la lista (manzana)
print(mis_frutas[4])  # Imprime el quinto elemento de la lista (uva)
print(mis_frutas[5])  # Imprime el sexto elemento de la lista (fresa)

# Imprimimos la longitud (cantidad de elementos) de la lista de frutas
print(len(mis_frutas))
