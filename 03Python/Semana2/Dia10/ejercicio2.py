# Creamos una lista vacía llamada lista.
lista = []

# Inicializamos una variable de control i con el valor 1.
i = 1

# Usamos un bucle while para agregar números del 1 al 100 a la lista.
while i <= 100:
    # Agregamos el valor de i a la lista.
    lista.append(i)
    # Incrementamos el valor de i en 1 para pasar al siguiente número.
    i = i + 1

# Imprimimos la lista completa.
print(lista)
