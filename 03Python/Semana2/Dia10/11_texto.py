# Definimos una variable de texto.

texto = "Ejemplo de una variable de texto."

# Convertimos el texto a mayúsculas utilizando el método upper() que convierte todas las letras del texto a mayúsculas.
texto_mayuscula = texto.upper()
print(texto_mayuscula)

# Convertimos el texto a minúsculas utilizando el método lower() que convierte todas las letras del texto a minúsculas.
texto_minuscula = texto.lower()
print(texto_minuscula)

# Capitalizamos el texto utilizando el método capitalize() que convierte la primera letra del texto a mayúscula y el resto a minúsculas.
texto_capitalize = texto.capitalize()
print(texto_capitalize)

# Intercambiamos las letras del texto utilizando el método swapcase() que cambia las mayúsculas a minúsculas y viceversa.
texto_swap = texto.swapcase()
print(texto_swap)

# Convertimos el texto a título utilizando el método title() que convierte la primera letra de cada palabra del texto a mayúscula.
texto_titulo = texto.title()
print(texto_titulo)
