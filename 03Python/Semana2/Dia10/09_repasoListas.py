# Creamos una lista de números
mis_numeros = [24, 38, 85, 19]

# Imprimimos cada elemento de la lista de números individualmente
print(mis_numeros[0])  # Imprime el primer elemento de la lista
print(mis_numeros[1])  # Imprime el segundo elemento de la lista
print(mis_numeros[2])  # Imprime el tercer elemento de la lista
print(mis_numeros[3])  # Imprime el cuarto elemento de la lista

# Usamos un bucle for para iterar sobre cada elemento de la lista y imprimirlo
# La variable 'numero' toma el valor de cada elemento en cada iteración del bucle
for numero in mis_numeros:
    print(numero)  # Imprime cada número de la lista uno por uno
