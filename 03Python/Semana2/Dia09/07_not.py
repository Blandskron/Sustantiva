# Definición de variables
puerta_abierta = True  # Variable booleana que indica si la puerta está abierta o no
dia = True  # Variable booleana que indica si es de día o no

# Verificación de la condición de la puerta abierta
if not puerta_abierta:
    # Si la puerta no está abierta, imprime el mensaje
    print("La puerta está abierta")

# Verificación de la condición de día
if not dia:
    # Si no es de día, imprime el mensaje
    print("Es de día")
