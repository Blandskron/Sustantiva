# Definir una lista de números enteros
numeros = [1, 2, 3, 4, 5]

# Leer la lista
print("Los números en la lista son:")
for numero in numeros:
    print(numero)

# Consumir la lista
# Calcular la suma de los números en la lista
suma_numeros = sum(numeros)
print("La suma de los números en la lista es:", suma_numeros)
