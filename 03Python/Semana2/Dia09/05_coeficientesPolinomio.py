# Definir una tupla con los coeficientes de un polinomio: ax^2 + bx + c
coeficientes = (2, -3, 1)

# Desempaquetar la tupla para obtener los coeficientes a, b y c
a, b, c = coeficientes

# Imprimir los coeficientes del polinomio
print("Coeficientes del polinomio:")
print("a =", a)
print("b =", b)
print("c =", c)
