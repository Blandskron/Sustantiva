# Definir variables que contienen diferentes tipos de datos compuestos
list_var = [1, 2, 3, 4]        # Lista: una secuencia ordenada y mutable de elementos
tuple_var = (1, 2, 3, 4)       # Tupla: una secuencia ordenada e inmutable de elementos
set_var = {1, 2, 3, 4}         # Conjunto: una colección desordenada y no indexada de elementos únicos
dict_var = {"a": 1, "b": 2, "c": 3}   # Diccionario: una colección de pares clave-valor, donde cada clave es única

# Imprimir los elementos de los tipos de datos compuestos
print(list_var)                # Imprime la lista [1, 2, 3, 4]
print(tuple_var)               # Imprime la tupla (1, 2, 3, 4)
print(set_var)                 # Imprime el conjunto {1, 2, 3, 4}
print(dict_var)                # Imprime el diccionario {'a': 1, 'b': 2, 'c': 3}
