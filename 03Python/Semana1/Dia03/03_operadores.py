# Operadores aritméticos
a = 5
b = 2
print("Suma:", a + b)           # Suma: 7
print("Resta:", a - b)          # Resta: 3
print("Multiplicación:", a * b) # Multiplicación: 10
print("División:", a / b)       # División: 2.5 (la división siempre devuelve un flotante)

# Operadores de comparación
x = 10
y = 20
print("Mayor que:", x > y)      # Mayor que: False
print("Menor que:", x < y)      # Menor que: True
print("Igual a:", x == y)       # Igual a: False
print("Diferente de:", x != y)  # Diferente de: True

# Operadores lógicos
print("AND lógico:", (x > 5) and (y < 30))   # AND lógico: True (ambas condiciones son verdaderas)
print("OR lógico:", (x > 5) or (y < 10))     # OR lógico: True (al menos una de las condiciones es verdadera)
