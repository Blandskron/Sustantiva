# Asignación de variables con diferentes tipos de datos
nombre = "Juan"  # Tipo de dato: cadena de caracteres (string)
edad = 30        # Tipo de dato: entero (integer)
altura = 1.75    # Tipo de dato: flotante (float)
es_estudiante = True  # Tipo de dato: booleano (boolean)

# Imprimir información utilizando las variables
print("Nombre:", nombre)
print("Edad:", edad)
print("Altura:", altura)
print("Es estudiante:", es_estudiante)

# Sentencia condicional if-else para tomar decisiones
if edad >= 18:
    print("¡Eres mayor de edad!")
else:
    print("Eres menor de edad.")

# Sentencia de bucle while para repetir acciones mientras se cumpla una condición
contador = 0
while contador < 5:
    print("Contador:", contador)
    contador += 1

# Sentencia de bucle for para iterar sobre una secuencia de elementos
colores = ["rojo", "verde", "azul", "amarillo"]
for color in colores:
    print("Color:", color)
