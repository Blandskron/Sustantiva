# Definir diferentes variables con diferentes tipos de datos
integer_var = 42           # Variable de tipo entero (int)
float_var = 3.14           # Variable de tipo flotante (float)
string_var = "Hola, soy una cadena"  # Variable de tipo cadena de texto (str)
boolean_var = True         # Variable de tipo booleano (bool)

# Imprimir los tipos de datos de las variables
print(type(integer_var))   # Imprime <class 'int'>, que indica que integer_var es de tipo int
print(type(float_var))     # Imprime <class 'float'>, que indica que float_var es de tipo float
print(type(string_var))    # Imprime <class 'str'>, que indica que string_var es de tipo str
print(type(boolean_var))   # Imprime <class 'bool'>, que indica que boolean_var es de tipo bool
