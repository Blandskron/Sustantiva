print(10 * 2)          # Multiplicación: 20
print(10 / 2)          # División: 5.0
print(10 % 2)          # Módulo: 0 (el resto de la división)
print(10 / 3)          # División: 3.3333333333333335
print(10 % 3)          # Módulo: 1 (el resto de la división)
print(10 // 3)         # División entera: 3 (parte entera del resultado)
print(2 ** 3)          # Exponenciación: 8 (2 elevado a la 3)
