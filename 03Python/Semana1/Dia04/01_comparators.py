# Mayor que (>)
print(7 > 3)   # True, ya que 7 es mayor que 3
print(3 > 7)   # False, ya que 3 no es mayor que 7
print(7 > 7)   # False, ya que 7 no es mayor que 7

# Menor que (<)
print(5 < 6)   # True, ya que 5 es menor que 6
print(6 < 5)   # False, ya que 6 no es menor que 5
print(5 < 5)   # False, ya que 5 no es menor que 5

# Mayor o igual que (>=)
print(2 >= 1)  # True, ya que 2 es mayor o igual que 1
print(2 >= 3)  # False, ya que 2 no es mayor o igual que 3
print(2 >= 2)  # True, ya que 2 es igual a 2

# Menor o igual que (<=)
print(1 <= 2)  # True, ya que 1 es menor o igual que 2
print(2 <= 1)  # False, ya que 2 no es menor o igual que 1
print(2 <= 2)  # True, ya que 2 es igual a 2

# Igual que (==)
print(6 == 6)  # True, ya que 6 es igual a 6
print(5 == 2)  # False, ya que 5 no es igual a 2

# No igual que (!=)
print(6 != 10) # True, ya que 6 no es igual a 10
print(6 != 6)  # False, ya que 6 es igual a 6

# Comparación de cadenas y tipos diferentes
print("Apple" == 'Apple')  # True, ya que ambas cadenas son iguales
print("Apple" == 'apple')  # False, ya que la comparación distingue entre mayúsculas y minúsculas
print("1" == 1)             # False, ya que los tipos son diferentes

# Evaluación de expresiones
age = 15
print(age >= 18)            # False, ya que la edad es menor que 18
