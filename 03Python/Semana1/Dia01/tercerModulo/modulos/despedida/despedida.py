def despedirse(nombre):
    """
    Esta función recibe un nombre como argumento
    y muestra un mensaje de despedida personalizado en la consola.
    
    Args:
    - nombre: Una cadena que representa el nombre del destinatario de la despedida.
    """
    # Crear un mensaje de despedida personalizado utilizando f-strings
    mensaje = f"Adiós, {nombre}. ¡Esperamos verte de nuevo pronto!"
    
    # Imprimir el mensaje de despedida en la consola
    print(mensaje)
