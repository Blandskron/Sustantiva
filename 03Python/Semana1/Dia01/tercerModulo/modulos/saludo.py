def saludar(nombre):
    """
    Esta función recibe un nombre como argumento
    y muestra un mensaje de saludo personalizado en la consola.
    
    Args:
    - nombre: Una cadena que representa el nombre del destinatario del saludo.
    """
    # Crear un mensaje de saludo personalizado utilizando f-strings
    mensaje = f"¡Hola, {nombre}! Bienvenido al mundo de los saludos en Python."
    
    # Imprimir el mensaje de saludo en la consola
    print(mensaje)
