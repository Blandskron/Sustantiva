# Mostrar texto simple
print("Hola, estudiantes!")

# Mostrar variables y texto combinados
nombre = "Juan"
edad = 20
print("Nombre:", nombre, "Edad:", edad)

# Mostrar resultados de operaciones
suma = 5 + 3
print("Resultado de la suma:", suma)

# Mostrar texto con formato
curso = "Programación en Python"
print("Estamos aprendiendo {}".format(curso))

# Mostrar varios elementos usando f-strings (formato a partir de Python 3.6)
print(f"{nombre} tiene {edad} años y está aprendiendo {curso}")

# Mostrar una lista
frutas = ["Manzana", "Banana", "Naranja"]
print("Lista de frutas:", frutas)

# Mostrar un diccionario
persona = {"nombre": "María", "edad": 25, "ciudad": "Ciudad de México"}
print("Información personal:", persona)
