# Definimos una función llamada saludar que acepta un argumento llamado nombre.
def saludar(nombre):
    # Concatenamos la cadena "Hola ", el nombre proporcionado y ", buenos días".
    # Luego, devolvemos esta cadena como resultado de la función.
    return "Hola " + nombre + ", buenos días"

