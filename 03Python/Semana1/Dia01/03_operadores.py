#OPERADORES ARITMETICOS

# Suma
resultado_suma = 5 + 3
print("Suma:", resultado_suma)

# Resta
resultado_resta = 5 - 3
print("Resta:", resultado_resta)

# Multiplicación
resultado_multiplicacion = 5 * 3
print("Multiplicación:", resultado_multiplicacion)

# División
resultado_division = 5 / 3
print("División:", resultado_division)

# División entera
resultado_division_entera = 5 // 3
print("División Entera:", resultado_division_entera)

# Residuo (Módulo)
resultado_modulo = 5 % 3
print("Residuo (Módulo):", resultado_modulo)

# Potencia
resultado_potencia = 5 ** 3
print("Potencia:", resultado_potencia)

#OPERADORES DE COMPARACION

# Igual a
igual_a = (5 == 3)
print("Igual a:", igual_a)

# No igual a
no_igual_a = (5 != 3)
print("No igual a:", no_igual_a)

# Mayor que
mayor_que = (5 > 3)
print("Mayor que:", mayor_que)

# Menor que
menor_que = (5 < 3)
print("Menor que:", menor_que)

# Mayor o igual que
mayor_o_igual_que = (5 >= 3)
print("Mayor o igual que:", mayor_o_igual_que)

# Menor o igual que
menor_o_igual_que = (5 <= 3)
print("Menor o igual que:", menor_o_igual_que)

#OPERADORES LOGICOS

# AND lógico
and_logico = (True and False)
print("AND lógico:", and_logico)

# OR lógico
or_logico = (True or False)
print("OR lógico:", or_logico)

# NOT lógico
not_logico = not True
print("NOT lógico:", not_logico)

#OPERADORES DE ASIGNACION

# Asignación simple
x = 5
print("Asignación simple:", x)

# Asignación con suma
x += 3
print("Asignación con suma:", x)

# Asignación con resta
x -= 2
print("Asignación con resta:", x)

# Asignación con multiplicación
x *= 4
print("Asignación con multiplicación:", x)

# Asignación con división
x /= 2
print("Asignación con división:", x)
