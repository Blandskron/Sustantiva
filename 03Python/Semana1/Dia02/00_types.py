# Asignación de un string a la variable my_name
my_name = "Nicolas"

# Reasignación de un string a la variable my_name (se sobrescribe el valor anterior)
my_name = 'Santiago'

# Imprimir el valor de la variable my_name
print('my_name =>', my_name)

# Imprimir el tipo de datos de la variable my_name
print(type(my_name))

# Asignación de un entero a la variable my_age
my_age = 12

# Imprimir el valor de la variable my_age
print('my_age =>', my_age)

# Imprimir el tipo de datos de la variable my_age
print(type(my_age))

# Asignación de un valor booleano a la variable is_single
is_single = False

# Imprimir el valor de la variable is_single
print('is_single =>', is_single)

# Imprimir el tipo de datos de la variable is_single
print(type(is_single))

# Capturar la entrada del usuario y almacenarla en la variable my_age (se asume que el usuario ingresará un valor numérico)
my_age = input('Cual es tu edad? ')

# Imprimir el valor de la variable my_age (siempre será una cadena de texto)
print('my_age =>', my_age)

# Imprimir el tipo de datos de la variable my_age (siempre será <class 'str'>, ya que input() devuelve una cadena de texto)
print(type(my_age))
